phrase = "coding bootcamp2025"

print("Is Alpha: ", phrase.isalpha())
print("Is Digit: ", phrase.isdigit())
print("Capitalize First Letter: ", phrase.capitalize())
print("Lowercase: ", phrase.lower())
print("Is Upper: ", phrase.isupper())
print(phrase.capitalize().center(50))
print(phrase.capitalize().ljust(50))
print(phrase.capitalize().rjust(50))
